# 6. Faça um programa que peça um CPF ao usuário e verifique se ele é válido.

# 7. Crie um jogo de adivinhação onde o computador escolhe um número aleatório entre 1 e 100 e o usuário tenta adivinhar com dicas de "maior" ou "menor".
