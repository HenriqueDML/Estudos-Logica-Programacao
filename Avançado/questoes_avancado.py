# 1. Crie um programa que leia 10 números e exiba o maior e o menor número digitado.

# 2. Desenvolva um programa que gere uma senha aleatória de 8 caracteres contendo letras e números.

# 3. Faça um programa que converta uma temperatura digitada em Celsius para Fahrenheit e Kelvin.

